    private void table(){
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("No");        
        tb.addColumn("Kode Karyawan");
        tb.addColumn("Nama Karyawan");
        karyawanTB.setModel(tb);
        
        try{
            ResultSet res = stat.executeQuery("SELECT * FROM `karyawan`");
            int no = 1;
            while(res.next())
            {
               tb.addRow(new Object[]{
                   no++,
                   res.getString("kode_karyawan"),
                   res.getString("nama_karyawan"),
               });
            }
        }catch (Exception e) {
            System.out.println("Gagal Query: " + e.getMessage());
        }
    }
    
