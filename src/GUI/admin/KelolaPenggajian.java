    private void hitungTotalBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungTotalBTNActionPerformed
        // TODO add your handling code here:
        double gajiPokok = Double.parseDouble(gajiPokokTF.getText());
        int kodeKaryawan = Integer.parseInt(kdTF.getText());
        int jumlahHadir = Integer.parseInt(jmTF.getText());
        int jumlahTerlambat = Integer.parseInt(jtTF.getText());
        int jumlahHadirTotal = Integer.parseInt(jhsTF.getText());

        
        int bulan = Integer.parseInt(bulanTF.getText());
        int tahun = Integer.parseInt(tahunTF.getText());
        
        YearMonth yearMonth = YearMonth.of(tahun, bulan);
        int jumlahHari = yearMonth.lengthOfMonth();
        
        int libu_m = jumlahHari - 4;
        
        //hitung Potong Terlambat
        int jpt = 50000;
        int hitungPotonganTerlambat = jumlahTerlambat * jpt;
        
        totalPotonganTerlambat.setText("" + hitungPotonganTerlambat);
        
        //hitung total gaji
        double gaji = ((double) jumlahHadirTotal / libu_m) * gajiPokok;

        // Membulatkan gaji ke nilai bulat terdekat
        long gajiBulat = Math.round(gaji);
        
        jumlahTotalGajiH.setText("" + gajiBulat);
        
        double totalpt = gajiBulat - hitungPotonganTerlambat;
        
        long totalPotonganB = Math.round(totalpt);
        
        totalPotongan.setText("" + totalPotonganB);
        
        int bonus = 0;
        if (!bonusTF.getText().isEmpty()) {
            bonus = Integer.parseInt(bonusTF.getText());
        }else{
            bonusTF.setText("0");
        }
        
        double hitungTotalGaji = totalpt + bonus;
        
        long TotalGaji = Math.round(hitungTotalGaji);
        
        totalTF.setText("" + TotalGaji);
    }//GEN-LAST:event_hitungTotalBTNActionPerformed

