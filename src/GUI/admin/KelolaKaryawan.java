        int id_jadwal = 0;
        if(jkm == "Pagi"){
            id_jadwal = 1;
        }else if(jkm == "Siang"){
            id_jadwal = 2;
        }else if(jkm == "Full"){
            id_jadwal = 3;
        }
        String tampilan = "yyyy-MM-dd";
        SimpleDateFormat fm = new SimpleDateFormat(tampilan);
