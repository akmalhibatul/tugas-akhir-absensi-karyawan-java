    private void karyawanTbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_karyawanTbMouseClicked
        // TODO add your handling code here:
        int row = karyawanTb.getSelectedRow();
        if(row >= 0){
            kodeKaryawanTF.setText(karyawanTb.getValueAt(row, 0).toString());
            NamaKaryawanTF.setText(karyawanTb.getValueAt(row, 1).toString());
            
            String jk = karyawanTb.getValueAt(row, 2).toString();
            if (jk.contains("Laki-laki")) {
                lkRB.setSelected(true);
            }else if (jk.contains("Perempuan")) {
                prRB.setSelected(true);
            }
            
            String jabatan = karyawanTb.getValueAt(row, 3).toString();
            if (jabatan.equals("Kepala Toko")) {
                jabatanCB.setSelectedIndex(1);
            } else if (jabatan.equals("Kasir")) {
                jabatanCB.setSelectedIndex(2);
            }   else if (jabatan.equals("Pramuniaga")) {
                jabatanCB.setSelectedIndex(3);
            }   else if (jabatan.equals("Packer")) {
                jabatanCB.setSelectedIndex(4);
            }   else if (jabatan.equals("Security")) {
                jabatanCB.setSelectedIndex(5);
            }
            
            String jam_kerja = karyawanTb.getValueAt(row, 8).toString();
            if (jam_kerja.equals("Pagi")) {
                jamKerjaCB.setSelectedIndex(1);
            } else if (jam_kerja.equals("Siang")) {
                jamKerjaCB.setSelectedIndex(2);
            }   else if (jam_kerja.equals("Full")) {
                jamKerjaCB.setSelectedIndex(3);
            }
            
            String tanggalSt = karyawanTb.getValueAt(row, 4).toString();
            try {
                // Mengonversi tanggalString menjadi objek Date
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                Date tanggal = dateFormat.parse(tanggalSt);

                // Mengatur nilai tanggal pada JDateChooser
                tglLahirDT.setDate(tanggal);
            } catch (ParseException e) {
                e.printStackTrace();
            }
            
            telpTF.setText(karyawanTb.getValueAt(row, 5).toString());
            alamatTF.setText(karyawanTb.getValueAt(row, 6).toString());
            
        }
    }//GEN-LAST:event_karyawanTbMouseClicked

