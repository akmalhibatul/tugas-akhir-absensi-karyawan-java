    private void table(){
        DefaultTableModel tb = new DefaultTableModel();
        
        tb.addColumn("Kode Karyawan");
        tb.addColumn("Nama Karyawan");
        tb.addColumn("Jenis Kelamin");
        tb.addColumn("Jabatan");
        tb.addColumn("Tanggal Lahir");
        tb.addColumn("No Telp");
        tb.addColumn("Alamat");
        tb.addColumn("Golongan");
        tb.addColumn("Jadwal");
        karyawanTb.setModel(tb);
        
        try{
            ResultSet res = stat.executeQuery("SELECT * FROM `karyawan` INNER JOIN golongan ON karyawan.id_golongan = golongan.id_golongan INNER JOIN jadwal ON karyawan.id_jadwal = jadwal.id_jadwal;");
            while(res.next())
            {
               tb.addRow(new Object[]{
                   res.getString("kode_karyawan"),
                   res.getString("nama_karyawan"),
                   res.getString("jenis_kelamin"),
                   res.getString("jabatan"),
                   res.getString("tanggal_lahir"),
                   res.getString("telepon"),
                   res.getString("alamat"),
                   res.getString("jabatan"),
                   res.getString("shift"),
               });
            }
        }catch (Exception e) {
            System.out.println("Gagal Query: " + e.getMessage());
        }
    }
    
